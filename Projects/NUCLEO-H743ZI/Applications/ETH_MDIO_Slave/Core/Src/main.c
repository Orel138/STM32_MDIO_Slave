/* MDIO Master -> WRITE PHY reg x -> IRQ -> HAL_MDIOS_WriteCpltCallback */
void HAL_MDIOS_WriteCpltCallback(MDIOS_HandleTypeDef *hmdios)
{
    uint32_t written;

    // Bitmap of registers written by MDIO master
    written = HAL_MDIOS_GetWrittenRegAddress(hmdios);

    for (uint32_t reg = 0; reg < PHY_REG_COUNT; reg++)
    {
        if (written & (1UL << reg))
        {
            uint16_t val;

            // Read DINn register written by master
            HAL_MDIOS_ReadReg(hmdios, reg, &val);

            // Store into PHY shadow registers
            phy_regs[reg] = val;

            /*!< IMPORTANT:
             The STM32 MDIOS peripheral serves MDIO READ data *before* the
             HAL_MDIOS_ReadCpltCallback() is executed (posted read behavior).
             This means the value returned to the MDIO master during a READ
             transaction is the content already present in the DOUT register.

             To ensure that a register written by the MDIO master is immediately
             readable on the very next READ access, we must proactively preload
             the DOUT register here, right after processing the WRITE transaction.
             Otherwise, the first READ after a WRITE would return the previous
             value, and the updated value would only appear on the second READ.
             */
            HAL_MDIOS_WriteReg(hmdios, reg, val);
        }
    }

    // Clear all write flags
    HAL_MDIOS_ClearWriteRegAddress(hmdios, written);
}

/* MDIO Master -> READ PHY reg x -> IRQ -> HAL_MDIOS_ReadCpltCallback */
void HAL_MDIOS_ReadCpltCallback(MDIOS_HandleTypeDef *hmdios)
{
    uint32_t read;

    // Bitmap of registers read by MDIO master
    read = HAL_MDIOS_GetReadRegAddress(hmdios);

    for (uint32_t reg = 0; reg < PHY_REG_COUNT; reg++)
    {
        if (read & (1UL << reg))
        {
            // Provide data to MDIO master
            HAL_MDIOS_WriteReg(hmdios, reg, phy_regs[reg]);
        }
    }

    // Clear all read flags
    HAL_MDIOS_ClearReadRegAddress(hmdios, read);
}


